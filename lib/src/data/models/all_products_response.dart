import 'package:scube_task/src/domain/entites/product_entity.dart';

/// ==========================================================
/// PRODUCT MODEL
/// ==========================================================
///
/// Represents a product fetched from the API, including its rating.
class Product {
  final int id;
  final String title;
  final double price;
  final String description;
  final String category;
  final String image;
  final Rating rating;

  Product({
    required this.id,
    required this.title,
    required this.price,
    required this.description,
    required this.category,
    required this.image,
    required this.rating,
  });

  factory Product.fromJson(Map<String, dynamic> json) {
    return Product(
      id: json['id'],
      title: json['title'],
      price: (json['price'] as num).toDouble(),
      description: json['description'],
      category: json['category'],
      image: json['image'],
      rating: Rating.fromJson(json['rating']),
    );
  }
}

class Rating {
  final double rate;
  final int count;

  Rating({
    required this.rate,
    required this.count,
  });

  factory Rating.fromJson(Map<String, dynamic> json) {
    return Rating(
      rate: (json['rate'] as num).toDouble(),
      count: json['count'],
    );
  }
}

/// ==========================================================
/// PRODUCT â†’ ENTITY MAPPER
/// ==========================================================
///
/// Converts [Product] model to [ProductEntity] for domain layer usage.
extension ProductMapper on Product {
  ProductEntity toEntity() {
    return ProductEntity(
      id: id,
      title: title,
      price: price,
      description: description,
      category: category,
      image: image,
      rating: rating.rate,
      ratingCount: rating.count,
    );
  }
}